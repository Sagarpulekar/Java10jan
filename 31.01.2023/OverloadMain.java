package com.demo;

public class OverloadMain {

	public static void main(String[] args) {

      System.out.println("In String main");
		
	}
	
	public static void main(int[] args) {
		
		System.out.println("In int main");

	}
	
   public static void main() {
		
		System.out.println("In int main");

	}


}
